package com.company;

public class while_loop {
    public static void main(String[] args) {
        System.out.println("Using while loop");
        int i = 1;
        while (i<=3){
            System.out.println(i);
            i++;
        }
        while(true){
            System.out.println("This is an infinite while loop");
        }
    }
}
