package com.company;

public class operators {
    public static void main(String[] args) {

        //modulo operator
        int a = 4;
        int b = 6 % a;
        System.out.println(b);

        //assignment operator
        int c =766;
        c *= 8;
        System.out.println(c);

        //comparison operator
        System.out.println(66==478);
        System.out.println(6757>=49);

        //logical operator
        System.out.println(47237>573 && 48<3);
        System.out.println(7426>847 || 372<4887);

        // bitwise operator
        System.out.println(45&3);

    }
}
