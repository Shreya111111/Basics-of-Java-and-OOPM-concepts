package com.company;

public class increment_decrement_operator {
    public static void main(String[] args) {
        //increment decrement operator
        int a = 43;
        System.out.println(a++);
        //first print then increment
        System.out.println(a);

        //first increment then prints
        System.out.println(++a);
        System.out.println(a);

        //similarly for decrement operator
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);

        int b = 77;
        System.out.println(++b *344);

        //increment decrement for char.
        char ch = 'S';
        System.out.println(++ch);
        System.out.println(--ch);




    }
}
